def sumaYresta(num1, num2):
    suma = num1 + num2
    resta = num1 - num2
    return suma, resta


def  main():
    suma, resta = sumaYresta(5, 9)
    print("La suma de 5 y 9 es:", suma)
    print("La resta de 5 y 9 es", resta)
    
if __name__ == "__main__":
    main()
