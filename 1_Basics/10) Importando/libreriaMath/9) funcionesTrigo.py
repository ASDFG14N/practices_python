import math
print("Funciones Trigoometricas")
print("------------------------\n\n")

#Todas las funciones trigonometricas tomas como parametro radianes y dvuelven radianes

#Para convertir grados a radianes, usamos el metodo .radians()
print(math.radians(90))

#Para convertir de radianes a graods usamos .degrees()
print(math.degrees(math.pi/2))

#La funcion seno se calcula con el metoso .sin()
print(math.sin(math.pi/2))

#La funcion coseno se calcula con el metodo cos()
print(math.cos(math.pi/2))

#La fucion tangente se calcula con el metodo .tan()
print(math.tan(math.pi/4))

#La funcion arcoseno se calcula con el metodo .asin()
print(math.asin(1))

#La funcion arcocoseno se calcula con el metodo .acos()
print(math.acos(0))

#La funcion arcotangente se calcula con el metodo .atan()
print(math.atan(1))