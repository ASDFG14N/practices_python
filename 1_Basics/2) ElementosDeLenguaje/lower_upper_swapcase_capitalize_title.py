string = 'Hola MuNdO'
print("Los metodos lower, upper, swapcase, capitalize")
print("----------------------------------------------\n")
print(string, "- palabra original")
print(f"Usando el metodo lower: {string.lower()}") #hace las palabras en minuscula
print(f"Usando el metodo upper: {string.upper()}") #hace que todas las palabras sean en mayuscula
print(f"Usando el metodo swapcase: {string.swapcase()}") #hace que se invierta, si la letra original es mayuscula
                                                         #la hace minuscual y viceversa
print(f"Usando el metodo capitalize: {string.capitalize()}") #Solo pone la primera letra en mayuscula y el resto
                                                             #en minusculas
print(f"Usando el metodo title: {string.title()}") #Pone todo el string en formato de titulo