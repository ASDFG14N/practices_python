#Invertir una lista significa que el primer elemento de una lista se convertira en el ultimo elemento,
#el segundo se convertira en el el penultimo y asi sucesivamente con los demas elementos de la lista
#Sintaxis ==> nombre_lista.reverse()
#El metodo reverse invierte la lista de manera permante
print("Metodo reverse()")
print("----------------")
vocales = ["a", "e", "i", "o", "u"]
print(f"Lista: {vocales}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"Lista: {vocales}")
print(f"posición 0: {vocales[0]}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"posición 0: {vocales[0]}")